from RUFAS.input_manager import InputManager
import numpy as np
from scipy.integrate import quad

im = InputManager()


class LactationCurve:
    """
    Class that represents the lacatation curve of the simulated farm. Obtains the necessary inputs from existing data collected in the simulation.

    ...

    Attributes
    ----------
    year: string
        The year which the simulation will end.
    region: string | None
        One of the 13 possible geographic regions or 'None' if the location is not included in the dataset.
    annual_MY_lbs: int | None
        The total milk yield generated by the farm in one year in lbs.
    parity_percentages: list
        Percentages of total herd represented by parity 1, 2, and 3+.
    num_milking_cows: float
        Number of milking cows in the herd. Calculated using the number of cows and assumed milking cow percentage.
    milking_freq: int
        The average or most common number of times cows are milked per day (1, 2, or 3 times daily)
    parity2_MilkYield305_adj: int
        The P2 milk adjustment value for the 305 day milk yield
    parity3_MilkYield305_adj: int
        The P3 milk adjustment value for the 305 day milk yield
    adjustment_dict: dict

    """

    def __init__(self):

        self.year = im.get_data("config.end_date")[:4]
        if int(self.year) > 2016:
            self.year = "2016"

        region_dict = im.get_data("lactation.fips_region")
        FIPS_code = im.get_data("config.FIPS_county_code")
        FIPS_state_code = None
        if FIPS_code != None:
            FIPS_state_code = int(FIPS_code / 1000)
        self.region = None
        if FIPS_state_code != None:
            for code_region in region_dict:
                if code_region["code"] == FIPS_state_code:
                    self.region = code_region["region"]

        self.annual_MY_lbs = im.get_data("animal.herd_information.annual_milk_yield_lbs")  # int or None
        self.parity_percentages = im.get_data("animal.herd_information.parity_percentages")  # list of 3 floats
        self.num_milking_cows = im.get_data("animal.herd_information.cow_num") * im.get_data(
            "lactation.assumed_milking_cow_percentage"
        )

        self.milking_freq = im.get_data("animal.animal_config.management_decisions.cow_times_milked_per_day")
        if self.milking_freq >= 2.5:
            self.milking_freq = "3x/d"
        else:
            self.milking_freq = "2x/d"

        # Assuming Y = 1632 and Z = 2196 based on the given assumptions
        self.parity2_MilkYield305_adj = im.get_data("lactation.parity_milk_adjustments.parity2_MilkYield305_adjustment")
        self.parity3_MilkYield305_adj = im.get_data("lactation.parity_milk_adjustments.parity3_MilkYield305_adjustment")

        self.adjustment_dict = im.get_data("lactation.adjustment_dict")

    def get_y_values_wood_curve(
        self, t: float, parameter_a: float, parameter_b: float, parameter_c: float
    ) -> np.float64:
        """
        Function that represents the milk yield at time t using parameters a, b, and c as the Wood's lactation curve parameters.

        Parameters
        ----------
        t : float
            Time.
        parameter_a: float
            Wood's lactation curve parameter a/l.
        parameter_b: float
            Wood's lactation curve parameter b/m.
        parameter_c: float
            Wood's lactation curve parameter c/n.

        Returns
        -------
        numpy.float64
            Milk yield at time t.

        """
        return parameter_a * np.power(t, parameter_b) * np.exp(-1 * parameter_c * t)

    def calc_integral_wood_curve(self, parameter_a: float, parameter_b: float, parameter_c: float) -> float:
        """
        Calculates the total milk yield from day 1 to day 305 of the lactation.

        Parameters
        ----------
        parameter_a: float
            Wood's lactation curve parameter a/l.
        parameter_b: float
            Wood's lactation curve parameter b/m.
        parameter_c: float
            Wood's lactation curve parameter c/n.

        Returns
        -------
        float
            Total milk yield from day 1 to day 305 of the lactation.

        """

        result, _ = quad(self.get_y_values_wood_curve, 1, 305, args=(parameter_a, parameter_b, parameter_c))
        return result

