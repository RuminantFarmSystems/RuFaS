class AnimalModuleConstants:
    """
    A class used to store constants related to an animal module.

    Attributes
    ----------
    DRY_FAR_OFF_START_DATE : int
        The default day number when the far-off dry period starts for cows.
    DRY_CLOSE_UP_START_DATE : int
        The default day number when the close-up dry period starts for cows.
    DEFAULT_NUM_STALLS : int
        The default number of stalls when creating a new pen.
    DEFAULT_MAX_STOCKING_DENSITY : float
        The default maximum stocking density when creating a new pen.

    """

    DRY_FAR_OFF_START_DATE = 218
    DRY_CLOSE_UP_START_DATE = 257
    DEFAULT_NUM_STALLS = 850
    DEFAULT_MAX_STOCKING_DENSITY = 1.2
